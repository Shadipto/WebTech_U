console.log("Primitive Data Types in JavaScript:");

// integer

let a = 10;
console.log(`Value of a: ${a}`);
console.log(`Datatype of a: ${typeof a}`);

// float

let b = 3.14;
console.log(`Value of b: ${b}`);
console.log(`Datatype of b: ${typeof b}`);

// boolean

let isHappy = true;
console.log(`Value of isHappy: ${isHappy}`);
console.log(`Datatype of isHappy: ${typeof isHappy}`);

// string

let name = "Shadipto";
console.log(`Value of name: ${name}`);
console.log(`Datatype of name: ${typeof name}`);

// null

let x = null;
console.log(`Value of x: ${x}`);
console.log(`Datatype of x: ${typeof x}`); // typeof null returns "object" due to a historical bug in JavaScript

// undefined

let y;
console.log(`Value of y: ${y}`);
console.log(`Datatype of y: ${typeof y}`);
